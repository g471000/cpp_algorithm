#include <iostream>

using namespace std;

int main()
{
	int firstArg;
	int secondArg;
	cout << "Enter first argumetn: ";
	cin >> firstArg; 
	cout << "Enter second argumetn: ";
	cin >> secondArg; 

	cout << firstArg << " * " << secondArg << " = " << firstArg * secondArg << endl;
	cout << firstArg << " + " << secondArg << " = " << firstArg + secondArg << endl;
	cout << firstArg << " / " << secondArg << " = " << firstArg / secondArg << endl;
	cout << firstArg << " - " << secondArg << " = " << firstArg - secondArg << endl;
}
